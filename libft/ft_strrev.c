#include "libft.h"

char	*ft_strrev(char *s)
{
	int		j;
	char	c;
	int		i;

	j = 0;
	i = 0;
	if (!s)
		return (NULL);
	while (s[i + 1])
		i++;
	while (i > j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		i--;
		j++;
	}
	return (s);
}
