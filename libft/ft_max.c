int		ft_max(int *str, int len)
{
	int		max;
	int		i;

	if (len != 0)
	{
		i = 0;
		max = str[i];
		while (i < len)
		{
			if (str[i] > max)
				max = str[i];
			i++;
		}
		return (max);
	}
	else
		return (0);
}
